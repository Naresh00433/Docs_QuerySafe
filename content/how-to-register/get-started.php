<h2>Get Started</h2>
<p>To begin your chatbot journey with QuerySafe:</p>
<ol>
  <li>Open your browser and go to the <a href="https://querysafe.in/" target="_blank" class="text-primary">QuerySafe </a>official website.</li>
  <li>On the homepage, click the <a href="https://querysafe.in/register/" target="_blank" class="text-primary">“Get Started”</a> button located at the top-right corner.</li>
  <li>You’ll be redirected to the <a href="https://querysafe.in/register/" target="_blank" class="text-primary">Sign-Up</a> page where you can create your account.</li>
</ol>
<p> Tip: Make sure you’re on a secure internet connection for the best experience.</p>
