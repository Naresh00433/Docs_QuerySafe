<h2>Sign Up</h2>
<p>On the <a href="https://querysafe.in/register/" target="_blank" class="text-primary">Sign-Up</a> page, fill in your details to create a secure account:</p>
<ul>
    <li>Full Name</li>
    <li>Email Address</li>
    <li>Password</li>
    <li>Confirm Password</li>
</ul>
<img src="../assets/images/signup.jpeg" alt="Homepage with 'Sign Up' button" style="max-width:100%;height:auto; margin-bottom:20px;">
<p>Check the Terms & Conditions box after reading them thoroughly.</p>
<p>Then, click the “<a href="https://querysafe.in/register/" target="_blank" class="text-primary">Sign Up</a>” button to create your account.</p>
<p>Once done, you’ll automatically be redirected to the <a href="https://querysafe.in/login/" target="_blank" class="text-primary">Sign-In</a> page.</p>