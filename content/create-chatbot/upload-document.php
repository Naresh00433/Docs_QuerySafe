<h2>Step 2: Upload Relevant Documents</h2>
<p>Your chatbot needs context — and that comes from your documents!</p>
<ul>
    <li>Click on “Upload Documents.”</li>
    <li>Select one or more files (PDF, DOCX, TXT).</li>
    <li>Each document should be under 10MB in size.</li>
    <li>You can upload multiple files to expand the chatbot’s knowledge base.</li>
</ul>