
<h2>2. Large Contentful Paint (LCP)</h2>
<p>
    <strong>Definition:</strong> Measures when the main content area of the chatbot (like the chat window, greeting message, or document response area) is fully visible.
</p>
<p>
    A fast LCP ensures your chatbot appears fully usable faster, improving the initial impression.
</p>
<p>
    Especially important for content-heavy bots that use media, document previews, or long text blocks.
</p>