
<h2>3. Total Blocking Time (TBT)</h2>
<p>
    <strong>Definition:</strong> TBT measures the time your chatbot takes to become responsive after the page starts loading.
</p>
<p>
    High TBT can lead to input lags or message delays, creating frustration for users.
</p>
<p>
    QuerySafe optimizes TBT to keep chat interactions fast and real-time.
</p>